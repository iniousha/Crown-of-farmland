package edu.kit.kastel.model.merge;

/**
 * this enum represents the compatibility type between two units of the same team.
 * @author ucktt
 */
public enum CompatibilityType {
    /**
     * units are symbiotically compatible.
     */
    SYMBIOTIC,
    /**
     * units are conspiratorially compatible.
     */
    CONSPIRATORIAL,
    /**
     * units are prime compatible.
     */
    PRIME,
    /**
     * units are incompatible.
     */
    INCOMPATIBLE
}
